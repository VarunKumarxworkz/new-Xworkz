package com.xwrokz.Gym.Dto;

public class GymDto {

	private String GymName;
	private int fees;
	private String ProtienBrand;
	private int ProtienCost;
	private int EquipmentPrice;
	public String getGymName() {
		return GymName;
	}
	public void setGymName(String gymName) {
		GymName = gymName;
	}
	public int getFees() {
		return fees;
	}
	public void setFees(int fees) {
		this.fees = fees;
	}
	public String getProtienBrand() {
		return ProtienBrand;
	}
	public void setProtienBrand(String protienBrand) {
		ProtienBrand = protienBrand;
	}
	public int getProtienCost() {
		return ProtienCost;
	}
	public void setProtienCost(int protienCost) {
		ProtienCost = protienCost;
	}
	public int getEquipmentPrice() {
		return EquipmentPrice;
	}
	public void setEquipmentPrice(int equipmentPrice) {
		EquipmentPrice = equipmentPrice;
	}
	
	
	
	
	
	
	
	
	
	
	
}
